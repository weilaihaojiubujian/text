package use;

public class user {
private String username;
private String pass;
private String 文章;
private String 作者;
private String 评论人;
private String 评论;
private int id;
public String getUsername() {
	return username;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public void setUsername(String username) {
	this.username = username;
}
public String getPass() {
	return pass;
}
public void setPass(String pass) {
	this.pass = pass;
}
public String get文章() {
	return 文章;
}
public void set文章(String 文章) {
	this.文章 = 文章;
}
public String get作者() {
	return 作者;
}
public void set作者(String 作者) {
	this.作者 = 作者;
}
public String get评论人() {
	return 评论人;
}
public void set评论人(String 评论人) {
	this.评论人 = 评论人;
}
public String get评论() {
	return 评论;
}
public void set评论(String 评论) {
	this.评论 = 评论;
}

}
